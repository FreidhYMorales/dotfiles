    border-top: none !important;
    padding: 4px !important;
    margin: 0px 0px 7px 0px !important;
}

.searchbar-engine-one-off-item {
    border-radius: 8px !important;
    margin-right: 3px !important;
}

#urlbar-anon-search-settings {
    margin-right: 0px !important;
}

/* ── Tabs ──────────────────────────────────────────────────────────────────── */
.tabbrowser-tab[pending] {
    filter: grayscale(1);
    opacity: 0.5;
}

/* ── Animations ────────────────────────────────────────────────────────────── */
:is(
    .tab-background,
    .toolbarbutton-icon,
    .toolbarbutton-badge-stack,
    .toolbarbutton-1,
    .bookmark-item,
    .PanelUI-zen-profiles-item,
    .download-state,
    .urlbarView-row,
    .urlbarView-action,
    .searchbar-engine-one-off-item,
    #urlbar-search-mode-indicator,
    #tracking-protection-icon-container,
    #page-action-buttons > *,
    #identity-box > *,
    toolbarbutton,
    toolbaritem,
    button,
    menu,
    menuitem,
    tab
):not(#urlbar-container, #personal-bookmarks) {
    transition: all 0.15s ease !important;

    &:is(tab) {
        transition: scale 0.15s ease !important;
    }

    &:is(:active, :not(tab)[open]) {
        scale: 0.95 !important;
    }
}
EOF

        # Ensure userChrome.css is enabled in Zen (Firefox default is off)
        USER_JS="$ZEN_PROFILE/user.js"
        if ! grep -qF 'legacyUserProfileCustomizations' "$USER_JS" 2>/dev/null; then
            echo 'user_pref("toolkit.legacyUserProfileCustomizations.stylesheets", true);' >> "$USER_JS"
        fi
    fi
fi

# ── Chromium — generate global vars userstyle for Stylus ─────────────────────
# Wraps the already-generated userContent.css in UserStyle metadata so Stylus
# can import it as a global style that injects --mat-* into all pages.
# This replaces userContent.css (Firefox-only) for Chromium web apps.
CHROMIUM_VARS="$HOME/.config/matugen/output/chromium-vars.user.css"

if [[ -f "$ZEN_UC_SRC" ]]; then
    {
        cat << METAEOF
