#!/usr/bin/env bash
# Runs after matugen generates all templates.
# Reloads themed apps so changes take effect without restarting anything.

KITTY_THEME="$HOME/.config/kitty/matugen-theme.conf"

# ── GTK / system color scheme (propagates to GTK apps, Zen Browser, Electron) ──
COLOR_SCHEME="${1:-dark}"
gsettings set org.gnome.desktop.interface color-scheme "prefer-${COLOR_SCHEME}" 2>/dev/null || true

# ── Kitty — live reload via socket ───────────────────────────────────────────
if [[ -f "$KITTY_THEME" ]]; then
    for sock in /tmp/kitty-*; do
        [[ "$sock" =~ ^/tmp/kitty-[0-9]+$ ]] || continue
        kitten @ --to "unix:$sock" set-colors --all "$KITTY_THEME" 2>/dev/null || true
    done
fi

# ── btop — theme reload via SIGUSR2 ──────────────────────────────────────────
killall -USR2 btop 2>/dev/null || true

# ── Hyprland — reload config (picks up new colors.lua) ───────────────────────
hyprctl reload 2>/dev/null || true

# ── Zen Browser — copy CSS to active profile chrome/ ─────────────────────────
ZEN_CSS_SRC="$HOME/.config/matugen/output/zen-matugen.css"
ZEN_UC_SRC="$HOME/.config/matugen/output/userContent.css"

if [[ -f "$ZEN_CSS_SRC" ]]; then
    ZEN_PROFILE=$(python3 -c "
import configparser, os
p = configparser.ConfigParser()
p.read(os.path.expanduser('~/.config/zen/profiles.ini'))
# Install section is authoritative for the actual active profile
for s in p.sections():
    if s.startswith('Install'):
        path = p.get(s, 'Default', fallback=None)
        if path:
            print(os.path.expanduser('~/.config/zen/' + path))
            exit()
# Fall back to Default=1 in profile sections
for s in p.sections():
    if p.get(s, 'Default', fallback='0') == '1':
        print(os.path.expanduser('~/.config/zen/' + p[s].get('Path', '')))
        exit()
" 2>/dev/null)

    if [[ -n "$ZEN_PROFILE" && -d "$ZEN_PROFILE" ]]; then
        ZEN_CHROME="$ZEN_PROFILE/chrome"
        mkdir -p "$ZEN_CHROME"

        # zen-matugen.css — palette vars for userChrome.css (Level A: browser chrome)
        cp "$ZEN_CSS_SRC" "$ZEN_CHROME/zen-matugen.css"

        # userContent.css — M3 palette vars injected into all web pages
        # Stylus userstyles for WhatsApp / Notion reference these via var(--mat-*)
        [[ -f "$ZEN_UC_SRC" ]] && cp "$ZEN_UC_SRC" "$ZEN_CHROME/userContent.css"

        # userChrome.css — always regenerate; actual colors come from the imported zen-matugen.css
        cat > "$ZEN_CHROME/userChrome.css" << 'EOF'
/* Zen browser chrome theming — generated by matugen post-hook, do not edit manually */
@import "zen-matugen.css";

/* ── Color tokens ──────────────────────────────────────────────────────────── */
